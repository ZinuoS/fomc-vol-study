import requests

def fetch_fred_series(series_id: str, start: str = START_DATE) -> pd.Series:
    """
    Pull daily yield from FRED via official REST API (JSON response).
    Uses FRED_API_KEY for authentication. Caches to CSV; re-fetches if cache
    is empty or stale (older than 30 days and data would have updated).
    """
    cache = CACHE_DIR / "market" / f"fred_{series_id}.csv"

    # Use cache if it exists and has data
    if cache.exists():
        try:
            df = pd.read_csv(cache, index_col=0, parse_dates=True)
            s  = df.iloc[:, 0].replace(".", np.nan).astype(float).dropna()
            if len(s) > 50:
                s.name = series_id
                return s[s.index >= start]
        except Exception:
            pass  # fall through to API fetch

    # FRED REST API — JSON, no vintage-date quirk, returns full observation history
    url = (f"https://api.stlouisfed.org/fred/series/observations"
           f"?series_id={series_id}"
           f"&api_key={FRED_API_KEY}"
           f"&observation_start={start}"
           f"&file_type=json")
    try:
        resp = requests.get(url, timeout=30)
        resp.raise_for_status()
        data = resp.json()
        obs  = data.get("observations", [])
        if not obs:
            raise ValueError(f"No observations returned for {series_id}")
        idx  = [pd.Timestamp(o["date"]) for o in obs]
        vals = [float(o["value"]) if o["value"] not in (".", "") else np.nan
                for o in obs]
        s = pd.Series(vals, index=idx, name=series_id).dropna()
        s.to_csv(cache)
        print(f"    [{series_id}] fetched {len(s)} obs via FRED API")
        return s
    except Exception as e:
        print(f"  FRED API failed for {series_id}: {e}")
        return pd.Series(dtype=float, name=series_id)


