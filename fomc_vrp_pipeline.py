# %% ── 1c. Realized vol estimators ────────────────────────────────────────────

_LN2 = log(2.0)

def parkinson_var(H: float, L: float) -> float:
    """Parkinson (1980): Var_P = (ln(H/L))^2 / (4 ln 2)."""
    if H <= 0 or L <= 0 or H < L:
        return np.nan
    return (log(H / L)) ** 2 / (4 * _LN2)


def garman_klass_var(O: float, H: float, L: float, C: float) -> float:
    """Garman-Klass: 0.5*(ln H/L)^2 - (2ln2-1)*(ln C/O)^2. ~7x more efficient."""
    if any(x <= 0 for x in (O, H, L, C)) or H < L:
        return np.nan
    return 0.5 * (log(H / L)) ** 2 - (2 * _LN2 - 1) * (log(C / O)) ** 2


def rv_annualised(var_daily: float) -> float:
    """Annualise a daily variance estimate → % points."""
    if np.isnan(var_daily) or var_daily <= 0:
        return np.nan
    return sqrt(TRADING_DAYS * var_daily) * 100


def yield_change_var(y_series: pd.Series, date: pd.Timestamp,
                     window: int = 1) -> float:
    """
    Event-day yield-change vol for cash-only tenors (7Y, 20Y).
    Returns annualised |Δy| in bps — DIFFERENT unit from GK pp, flagged downstream.

    For window=1 (event day): uses |Δy| on the FOMC date × sqrt(252).
    This is the point-in-time realized move, not a multi-day std estimate,
    which avoids the ddof=1 NaN trap for single observations.
    For window>1: annualised std of the window's daily yield changes.
    """
    idx = y_series.index.searchsorted(date)
    # Get idx+1 lookback values plus 1 forward so we can difference across the event
    start = max(0, idx - window)
    end   = min(len(y_series), idx + 2)
    vals  = y_series.iloc[start:end].dropna().values
    if len(vals) < 2:
        return np.nan
    diffs = np.diff(vals) * 100    # yield changes in bps
    if len(diffs) == 1:
        # Single event-day move: annualise |Δy|
        return float(abs(diffs[0]) * sqrt(TRADING_DAYS))
    return float(np.std(diffs, ddof=1) * sqrt(TRADING_DAYS))


def compute_forward_rv_yield(y_series: pd.Series, event_date: pd.Timestamp,
                              k: int) -> float:
    """k-day forward close-to-close yield-change RV from event_date+1."""
    idx = y_series.index.searchsorted(event_date)
    fwd = y_series.iloc[idx + 1: idx + k + 2].dropna()
    if len(fwd) < 2:
        return np.nan
    diffs = np.diff(fwd.values) * 100  # bps
    return float(np.std(diffs, ddof=1) * sqrt(TRADING_DAYS))


# ── Black-76 model for futures options ────────────────────────────────────────
# Reference: Black (1976). For Treasury futures options (American; approximated
# as European ATM short-dated — early exercise premium negligible near-the-money).

from math import exp, pi as _PI
from scipy.stats import norm as _norm
from scipy.optimize import brentq as _brentq


def _b76_d1d2(F: float, K: float, T: float, sigma: float):
    d1 = (log(F / K) + 0.5 * sigma ** 2 * T) / (sigma * sqrt(T))
    return d1, d1 - sigma * sqrt(T)


def black76_call(F: float, K: float, r: float, T: float, sigma: float) -> float:
    """Black-76 call price on a futures contract."""
    if T <= 0 or sigma <= 0:
        return max(0.0, F - K) * exp(-r * T)
    d1, d2 = _b76_d1d2(F, K, T, sigma)
    return exp(-r * T) * (F * _norm.cdf(d1) - K * _norm.cdf(d2))


def black76_put(F: float, K: float, r: float, T: float, sigma: float) -> float:
    """Black-76 put price on a futures contract (put-call parity from call)."""
    return black76_call(F, K, r, T, sigma) - exp(-r * T) * (F - K)


def black76_atm_straddle(F: float, r: float, T: float, sigma: float) -> float:
    """ATM straddle price under Black-76 (K = F exactly)."""
    if T <= 0 or sigma <= 0:
        return 0.0
    d1 = 0.5 * sigma * sqrt(T)
    # ATM straddle = 2 × call(ATM) = 2 × e^(-rT) × F × [N(d1) - N(-d1)]
    return 2.0 * exp(-r * T) * F * (2.0 * _norm.cdf(d1) - 1.0)


def black76_iv_from_straddle(straddle_price: float, F: float,
                               r: float, T: float) -> float:
    """
    Invert ATM straddle price → Black-76 implied vol (σ) via Brent's method.
    Returns annualised σ as a decimal (e.g. 0.05 = 5% per year).
    """
    if straddle_price <= 0 or F <= 0 or T <= 0:
        return np.nan
    # Approx initial guess: straddle ≈ 2Fe^(-rT)σ√(T/2π)
    sigma0 = straddle_price * sqrt(2.0 * _PI / T) / (2.0 * F * exp(-r * T))
    try:
        return float(_brentq(
            lambda s: black76_atm_straddle(F, r, T, s) - straddle_price,
            1e-6, 20.0, xtol=1e-7, maxiter=80,
        ))
    except Exception:
        return float(np.clip(sigma0, 0.001, 5.0))


def iv_to_event_vol_pp(iv_ann: float, T_horizon: float = 30 / 252) -> float:
    """
    Convert a constant-horizon (30-day) annualized IV (as %) to
    expected 1-day event vol in pp using CBOE VIX-style formula.
    VIX-style: event_vol = IV × sqrt(T_horizon) [calendar horizon]
    For FOMC day: T_event = 1/252 → event_vol = IV × sqrt(1/252) [≈ IV/15.87]
    """
    return iv_ann * sqrt(T_horizon)   # in same units as IV (pp or %)


# %% ── 1d. Build realized_curve ───────────────────────────────────────────────

def build_realized_curve(fomc_dates: list, fred_yields: dict,
                          ohlc_data: dict) -> pd.DataFrame:
    """
    For every (meeting_date, tenor): compute event-day RV + k-day forward RV.

    Columns:
        rv_event_gk, rv_event_park  — futures-based (pp ann.); NaN for cash-only
        rv_event_yc                 — yield-change σ (bps ann.); non-null for all
        rv_fwd_{k}d                 — k-day forward yield-change RV (bps ann.)
        estimator_type              — 'gk_futures' | 'yc_cash' (cash-only tenors)
        cash_only_flag              — True when OHLC unavailable (7Y, 20Y)
    """
    rows = []
    for fd in fomc_dates:
        ts = pd.Timestamp(fd)
        for tenor, cfg in TENORS.items():
            row = {"meeting_date": ts, "tenor": tenor}
            is_cash = cfg.get("cash_only", False)
            row["cash_only_flag"] = is_cash
            row["estimator_type"] = "yc_cash" if is_cash else "gk_futures"

            # ── Futures GK + Parkinson (where available) ──────────────────────
            if not is_cash and tenor in ohlc_data:
                df_ohlc = ohlc_data[tenor]
                if ts in df_ohlc.index:
                    r  = df_ohlc.loc[ts]
                    O, H, L, C = float(r["Open"]), float(r["High"]), float(r["Low"]), float(r["Close"])
                    row["rv_event_gk"]   = rv_annualised(garman_klass_var(O, H, L, C))
                    row["rv_event_park"] = rv_annualised(parkinson_var(H, L))
                else:
                    row["rv_event_gk"] = row["rv_event_park"] = np.nan
            else:
                row["rv_event_gk"] = row["rv_event_park"] = np.nan

            # ── Yield-change vol (all tenors, different unit — FLAGGED) ───────
            if tenor in fred_yields:
                y  = fred_yields[tenor]
                row["rv_event_yc"] = yield_change_var(y, ts, window=1)
                for k in RV_WINDOWS:
                    row[f"rv_fwd_{k}d"] = compute_forward_rv_yield(y, ts, k)
            else:
                row["rv_event_yc"] = np.nan
                for k in RV_WINDOWS:
                    row[f"rv_fwd_{k}d"] = np.nan

            rows.append(row)

    df = pd.DataFrame(rows)
    df["meeting_date"] = pd.to_datetime(df["meeting_date"])
    return df.sort_values(["meeting_date", "tenor"]).reset_index(drop=True)


