# ── Pre-scored vocabulary loader ──────────────────────────────────────────────
# Populates filter + vol-dir caches from vrp_prescore_vocab.json BEFORE any API
# calls. This means the pipeline works fully offline — API adds extra precision.
_PRESCORE_VOCAB: dict = {}   # {word: {decision, reason, warm_score, teal_score}}

def load_prescore_file(path: Path = PRESCORE_FILE) -> int:
    """
    Load pre-scored FOMC vocabulary and seed the filter + vol-direction caches.
    Returns number of new entries added across both caches.
    """
    global _PRESCORE_VOCAB
    if not path.exists():
        return 0
    vocab = json.loads(path.read_text())
    _PRESCORE_VOCAB = vocab
    n = 0
    for word, meta in vocab.items():
        fkey = _sha(word, FILTER_RUBRIC_VER)
        if fkey not in _filter_cache:
            _filter_cache[fkey] = {
                "decision": meta.get("decision", "KEEP"),
                "reason":   meta.get("reason", "prescore"),
            }
            n += 1
    _save_cache(_filter_cache, _FILTER_CACHE_PATH)
    return n


